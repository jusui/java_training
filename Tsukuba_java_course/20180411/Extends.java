/*
【クラスの継承】
class A {
}    

class B extends A {
}

[クラスAとBの関係]
- AはBのスーパークラス(親クラス)である
- BはAのサブクラス(子クラス)である
- BはAを継承したクラスである
- BはAから派生したクラスである

A = 乗り物
B = 車

親クラスを呼ぶときは，
this.** -> super.**
*/

    
